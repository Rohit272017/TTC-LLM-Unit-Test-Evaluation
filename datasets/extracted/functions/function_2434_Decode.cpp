bool IsValidUtf8(std::string_view code_units) {
  using utf8_decode::kAccept;
  utf8_decode::State state = utf8_decode::kAccept;
  char32_t codep;
  for (const char x : code_units) {
    utf8_decode::Decode(&state, &codep, x);
  }
  return state == kAccept;
}
